    chat_id = message.chat.id
    welcome_message = """<b>–ü—Ä–∏–≤–µ—Ç! –Ø —Ç–≤–æ–π –ª–∏—á–Ω—ã–π –ø–æ–≥–æ–¥–Ω—ã–π –±–æ—Ç üå¶Ô∏è</b>\n\n–° –ø–æ–º–æ—â—å—é –º–µ–Ω—è —Ç—ã —Å–º–æ–∂–µ—à—å —É–∑–Ω–∞—Ç—å —Ç–µ–∫—É—â—É—é –ø–æ–≥–æ–¥—É –≤ –ª—é–±–æ–º –≥–æ—Ä–æ–¥–µ –º–∏—Ä–∞! –ü—Ä–æ—Å—Ç–æ –≤–≤–µ–¥–∏ –Ω–∞–∑–≤–∞–Ω–∏–µ –≥–æ—Ä–æ–¥–∞, –∏ —è –ø—Ä–µ–¥–æ—Å—Ç–∞–≤–ª—é —Ç–µ–±–µ –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏—é –æ —Ç–µ–º–ø–µ—Ä–∞—Ç—É—Ä–µ, —Å–∫–æ—Ä–æ—Å—Ç–∏ –≤–µ—Ç—Ä–∞, –≤–ª–∞–∂–Ω–æ—Å—Ç–∏ –∏ –æ–ø–∏—Å–∞–Ω–∏–∏ –ø–æ–≥–æ–¥—ã\n\nüí° <b>–ü—Ä–∏–º–µ—Ä—ã –∫–æ–º–∞–Ω–¥:</b>\n- <i>Moscow</i>\n- <i>New York</i>\n- <i>London</i>"""

